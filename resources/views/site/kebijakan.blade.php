        <h2>Izin Aplikasi (Android)</h2>
        <p>Untuk mendukung fitur dan layanan yang disediakan dalam Aplikasi, Aplikasi dapat meminta akses ke izin tertentu pada perangkat Anda. Izin ini hanya digunakan ketika diperlukan untuk fungsionalitas Aplikasi.</p>

        <p>Izin yang diminta oleh Aplikasi dapat mencakup:</p>

        <h3>1) Akses Internet</h3>
        <p><strong>Izin:</strong> android.permission.INTERNET</p>
        <p><strong>Tujuan:</strong> Digunakan untuk menghubungkan Aplikasi ke layanan online, memuat data, dan berkomunikasi dengan server atau layanan pihak ketiga (seperti layanan Google).</p>

        <h3>2) Akses Penyimpanan</h3>
        <p><strong>Izin:</strong></p>
        <ul>
            <li>android.permission.READ_EXTERNAL_STORAGE</li>
            <li>android.permission.WRITE_EXTERNAL_STORAGE</li>
            <li>android.permission.MANAGE_EXTERNAL_STORAGE</li>
        </ul>
        <p><strong>Tujuan:</strong> Digunakan untuk membaca, menyimpan, mengelola, dan menyimpan file yang terkait dengan fitur Aplikasi (seperti dokumen, data yang diekspor, gambar, atau file lain yang dihasilkan/digunakan oleh Aplikasi).</p>

        <h3>3) Akses Lokasi</h3>
        <p><strong>Izin:</strong></p>
        <ul>
            <li>android.permission.ACCESS_FINE_LOCATION</li>
            <li>android.permission.ACCESS_COARSE_LOCATION</li>
            <li>android.permission.ACCESS_BACKGROUND_LOCATION</li>
        </ul>
        <p><strong>Tujuan:</strong> Digunakan untuk mengakses lokasi perangkat Anda untuk fitur Aplikasi yang memerlukan informasi lokasi, termasuk tampilan peta, pelacakan lokasi, dan layanan berbasis geospasial. Data lokasi dapat dikumpulkan saat Aplikasi digunakan, dan (jika diaktifkan) juga dapat dikumpulkan di latar belakang untuk mendukung fitur yang memerlukan akses lokasi latar belakang.</p>

        <h3>4) Akses Kamera</h3>
        <p><strong>Izin:</strong> android.permission.CAMERA</p>
        <p><strong>Tujuan:</strong> Digunakan untuk memungkinkan Aplikasi mengakses kamera untuk mengambil foto atau tujuan pemindaian sebagai bagian dari fungsionalitas Aplikasi.</p>

