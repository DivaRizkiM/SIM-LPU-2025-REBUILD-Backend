            <li>Halaman Aplikasi yang Anda kunjungi, waktu dan tanggal kunjungan Anda, waktu yang dihabiskan di halaman tersebut</li>
            <li>Waktu yang dihabiskan di Aplikasi</li>
            <li>Sistem operasi yang Anda gunakan pada perangkat mobile Anda</li>
        </ul>

